def multiply_bits(a,b):
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        bit_set = a & 0x80
        a <<= 1
        a &= 0xFF
        if bit_set:
            a ^= 0x1b
        b >>= 1
    return p
